public class Animal {
    public void sleep() {
        System.out.println("Animal went to sleep");
        System.out.println("Animal woke up");
    }
    public void makeNoise() {
        System.out.println("Animal made a noise");
    }
    public void roam() {
        System.out.println("Animal started roaming");
        System.out.println("Animal stopped roaming");
    }
}
